export const messages: Record<Locale, Messages> = {
  en,
  vi,
};

export function normalizeLocale(value: string | null | undefined): Locale {
  return value === "vi" ? "vi" : "en";
}

export function getMessages(locale: Locale): Messages {
  return messages[locale];
}

export function getMessage(locale: Locale, key: MessageKey): string {
  return messages[locale][key];
}

export function roleLabel(locale: Locale, role: "owner" | "admin" | "member") {
  if (role === "owner") {
    return getMessage(locale, "roleOwner");
  }

  if (role === "admin") {
    return getMessage(locale, "roleAdmin");
  }

  return getMessage(locale, "roleMember");
}

export function statusLabel(
  locale: Locale,
  status: "active" | "completed" | "auto_closed" | "cancelled" | "needs_review",
) {
  if (status === "active") {
    return getMessage(locale, "statusActive");
  }
  if (status === "completed") {
    return getMessage(locale, "statusCompleted");
  }
  if (status === "auto_closed") {
    return getMessage(locale, "statusAutoClosed");
  }
  if (status === "cancelled") {
    return getMessage(locale, "statusCancelled");
  }
  return getMessage(locale, "statusNeedsReview");
}
